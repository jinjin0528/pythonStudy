star = int(input())

if 380 <= star < 425:
    print("Violet")
elif 425 <= star < 450 :
    print("Indigo")
elif 450 <= star < 495:
    print("Blue")
elif 495 <= star < 570 :
    print("Green")
elif 570 <= star < 590 :
    print("Yellow")
elif 590 <= star < 620 :
    print("Orange")
elif 620 <= star <= 780 :
    print("Red")